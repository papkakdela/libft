#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t len;

	len = ft_strlen(s) + 1;
	while (len--)
	{
		if (*s++ == c)
			return ((char *)(s - 1));
	}
	return (NULL);
}
