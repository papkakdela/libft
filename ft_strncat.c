#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *cs1;

	cs1 = (char*)s1;
	while (*cs1)
		cs1++;
	while (*s2 && n--)
		*cs1++ = *s2++;
	*cs1++ = '\0';
	return (s1);
}
